                  <p>When I created Ronson, the God of Apathy for a role playing game, it seemed natural to expand the idea into a full pantheon. The thing with Ronson is that he dressed like a guy from Earth - gray collard shirt, blue jeans and red and white Nike sneakers.</p>
                  <p>To explain that required a back story that eventually evolved into the story that is "The Gods of Arr-Kelaan". So I guess that's the direct inspiration.</p>
                  <p>But I've always enjoyed mythological stories, and have always had a tendency to think that way as far as my writing goes. Though I am by no means an expert in mythologies of the past, I do have read a lot of them.</p>
                  <p>I like the idea that instead of these stories being created to explain something as yet inexplicable in the real world, they were actual things that happened. The idea of gods mingling with the mortals they rule over is a pretty neat concept.</p>
                  <p>A better question than what inspired me is what keeps me interested in it? I have had several story ideas throughout my life, but this one always came back to me. I don't know why that is, but I feel a driving need to write and ultimately finish this story</p>

                  <p><strong>How long have you been doing G of A??</strong></p>
                  <p>On and off since 1986, I think. 1986 was the year I created Ronson for the D&D game on Len's planet Arr-Kelaan. But I've done and redone the story several times since then.</p>
                  <p>The revision that I finally decided "This is it, this is the starting point, no more do-overs" was in 1991. That story is called "For the Death of Kings." It was a total of 77 pages, split into 5 chapters (sort of). I think it took me a year and a half to finish it. I don't post it online because (a.) it's not as good as the stuff online and (b.) the word balloons are very hard to read when the quality is reduced to screen quality. I added it to the end of my first book "The Gods of Arr-Kelaan: Going Home (book one)".</p>
                  <p>I took a few years off, and then wrote and drew the "Myths & Legends" storyline. That was a 144 page story and it took me nearly two years.</p>
                  <p>After that, I was drawing a comic strip called "Drawing From Life" for a local newspaper, and that cut into a lot of my drawing time.</p>
                  <p>In 2003, I decided to start "The Gods of Arr-Kelaan" up again. My format was going to be half-size comics like the religious Chick Tracts that you see crumpled up in parking lots from time to time. My thought was that it would be half the printing costs of the previous two stories and if I made them short chapters I could possibly get people to buy them for a quarter. I hadn't figured out how to cut them, but figured it couldn't be that hard. These would be the first four chapters to "Going Home".</p>
                  <p>But that turned out not to be important. A friend of mine, Shawn, told me that a comic site called Modern Tales was looking for submissions. All I had to do was figure out how to post what I had online and they'd review it and tell me if they were interested.</p>
                  <p>So suddenly I had to learn how this internet thing worked. My brother Steve helped me out a lot and eventually I had "Myths & Legends" and the first few chapters of "Going Home" posted on geocities.</p>
                  <p>Modern Tales never contacted me, and geocities sucks, so eventually I started looking into getting decent hosting. Luckily, I came across the infant Drunk Duck and have been hosting here ever since November of 2003, I think.</p>

                  <p><strong>How long have you been making comics in general?</strong></p>
                  <p>My whole life. I have an old comic I made parodying superheroes drawn when I was about 8 or so. I've always liked the medium of comics.</p>

                  <p><strong>What process do you use to make your comics? (By hand? By computer? What programs? What tools? Etc.)</strong></p>
                  <p>I draw most of the pages by hand, inking directly over my pencils. Then I scan them into the computer with Adobe Photoshop. I add backgrounds which are either hand-drawn separately or drawn on the computer with Adobe Illustrator. I do all the lettering on Adobe Illustrator and use pretty much exclusively BlamBot fonts. (with the exception of my own "ChuckFont", which is used for the voices of bodiless souls). My brother Steve does all the coloring and grayscale toning as well as the special effects - mostly in Photoshop.</p>
                  <p>Hand Drawing Tools: Kuratake Brush Pen, Mechanical Pens (.1,.3,.5,.8), Felt tip pens, .5 mm blue led mechanical pencil.</p>
                  <p>Paper: Either 9x12 Bristol board or Hammermil A4 laser paper (10466-1)</p>
                  <p>Computer Programs: Adobe Illustrator, Adobe Photoshop, Corel Draw (occasionally)</p>

