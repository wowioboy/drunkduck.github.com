<p>
<b>1. What does the “FAVE” button mean? </b>
<br>
This adds the comic to your favorites.  When you’re reading a favorited comic, the button changes to “FAVED!” to let you know it’s already in your favorites.

<p>
<b>2. What does the envelope symbol mean? </b>
<br>
This button turns on e-mail notifications for the comic you’re reading.  The comic has to be in your favorites to use this feature.

<p>
<b>3. How do I use the “save my place” function?</b>
<br>
This is a great way to save your place in a comic, especially for those lengthy comics with 100 + pages, so that when you come back to it, your place is saved!

<p>
<b>4. What does the letter to the right of the tool bar mean?</b>
<br>
Ahhh – you may have known that we have had a rating system for the Duck, but never exposed it! E means that “Everyone” can view this comic, no censorship. T+ means that Content is suitable for teens or older. Mild violence, slightly mature themes. No obscenities, graphic violence, sex, or nudity. M means Content contains mature themes not suitable for children. May contain occasional obscenities, limited graphic violence, implied sex, mild nudity. No sexually explicit material, no graphic sexual acts, no Hentai, no unrelenting violence, or non-stop obscenities.  A means content is sexually explicit, exceedingly violent, or an excess use of obscenities.

<p>
<b>5. How do you choose your tool bar color?</b>
<br>
The link for this is found in the template area for your comic.  There are currently 15 styles to choose from.  Pick something that better compliments your comic pages or something that totally clashes!  The choice is yours.
